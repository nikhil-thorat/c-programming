#include <stdio.h>

int main() {

  // Array of Integers
  int array[5];

  // Assigning values at each index
  array[0] = 1;
  array[1] = 2;
  array[2] = 3;
  array[3] = 4;
  array[4] = 5;

  // Accessing element from the array
  int second_element = array[1];
  printf("Second element is %d", second_element);

  return 0;
}
